#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	size_t	i;

	dup = (char *)malloc((ft_strlen(src) + 1) * (sizeof(char)));
	if (!dup)
		return (0);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
